// Clean and parse Gemini JSON outputs
function parseDynamicNodeJSON(rawText) {
    try {
        // Clean markdown blocks
        let cleaned = rawText.trim();
        if (cleaned.startsWith("```")) {
            cleaned = cleaned.replace(/^```json/, "").replace(/^```/, "").replace(/```$/, "").trim();
        }
        
        // Find the first { and last } to extract JSON strictly
        const startIdx = cleaned.indexOf("{");
        const endIdx = cleaned.lastIndexOf("}");
        
        if (startIdx === -1 || endIdx === -1) {
            throw new Error("No JSON boundaries found in response");
        }
        
        cleaned = cleaned.substring(startIdx, endIdx + 1);
        return JSON.parse(cleaned);
    } catch (e) {
        console.warn("JSON Parse cleaning failed, attempting raw response parse:", e);
        // Fallback placeholder node in case parsing breaks
        return {
            text: "คือ... เมื่อกี้ฉันเผลอคิดอะไรเพลินไปหน่อยน่ะ คุยกันต่อเถอะนะ!",
            expression: "happy",
            affectionChange: 0,
            choices: [
                { id: "fallback_1", hint: "คุยเรื่องท้องฟ้ากันต่อ", next: "node_friendly", affectionChange: 1 },
                { id: "fallback_2", hint: "ชวนเขานั่งคุยสบายๆ", next: "node_sit", affectionChange: 1 }
            ]
        };
    }
}

// Gemini REST Call
async function callGeminiAPI(promptText) {
    const response = await fetch(API_URL, {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ prompt: promptText })
    });

    if (!response.ok) {
        const err = await response.json().catch(() => ({ error: response.statusText }));
        throw new Error(`API Error: ${response.status} - ${err.error || response.statusText}`);
    }

    const data = await response.json();
    if (data.text) {
        return data.text;
    } else {
        throw new Error("Invalid response from server");
    }
}

