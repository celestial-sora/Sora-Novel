// Load the dialogue tree JSON
async function loadDialogueTree() {
    try {
        const response = await fetch("dialogue_tree.json");
        if (!response.ok) throw new Error("Failed to load dialogue tree");
        gameState.dialogueTree = await response.json();
        console.log("Dialogue tree loaded successfully.");
    } catch (error) {
        console.error("Error loading dialogue tree:", error);
        showToast("ไม่สามารถโหลดไฟล์ Dialogue tree ได้!", "accent");
    }
}

// Check if a save exists in localStorage
function checkExistingSave() {
    const save = localStorage.getItem("oracle_chan_vn_save");
    if (save) {
        document.getElementById("btn-load-game").removeAttribute("disabled");
        document.getElementById("ctrl-load").removeAttribute("disabled");
    } else {
        document.getElementById("btn-load-game").setAttribute("disabled", "true");
        document.getElementById("ctrl-load").setAttribute("disabled", "true");
    }
}

// Sound Synthesizer using Web Audio API (creates beautiful retro 8-bit sound effects)
function initAudio() {
    try {
        if (!audioCtx) {
            audioCtx = new (window.AudioContext || window.webkitAudioContext)();
        }
    } catch (e) {
        console.warn("Web Audio API is not supported or blocked in this browser:", e);
        audioCtx = null;
    }
}

function playSound(type) {
    try {
        initAudio();
        if (!audioCtx) return;
        
        // Ensure audio context is active (handling browser autoplay policy)
        if (audioCtx.state === "suspended") {
            audioCtx.resume().catch(err => console.warn("AudioContext resume rejected:", err));
        }

        const osc = audioCtx.createOscillator();
        const gain = audioCtx.createGain();
        osc.connect(gain);
        gain.connect(audioCtx.destination);

        const now = audioCtx.currentTime;

        if (type === "click") {
            // Short cute pop sound
            osc.type = "sine";
            osc.frequency.setValueAtTime(500, now);
            osc.frequency.exponentialRampToValueAtTime(150, now + 0.1);
            gain.gain.setValueAtTime(0.1, now);
            gain.gain.linearRampToValueAtTime(0.01, now + 0.1);
            osc.start(now);
            osc.stop(now + 0.1);
        } else if (type === "tick") {
            // Dialogue text typing sound (very quiet tick)
            osc.type = "sine";
            osc.frequency.setValueAtTime(600, now);
            gain.gain.setValueAtTime(0.01, now);
            gain.gain.linearRampToValueAtTime(0, now + 0.03);
            osc.start(now);
            osc.stop(now + 0.03);
        } else if (type === "ding") {
            // Sweet arpeggio chime for positive affection
            osc.type = "sine";
            osc.frequency.setValueAtTime(880, now);
            osc.frequency.setValueAtTime(1320, now + 0.08);
            gain.gain.setValueAtTime(0.12, now);
            gain.gain.exponentialRampToValueAtTime(0.005, now + 0.35);
            osc.start(now);
            osc.stop(now + 0.4);
        } else if (type === "sad") {
            // Low metallic chord for negative affection
            osc.type = "triangle";
            osc.frequency.setValueAtTime(220, now);
            osc.frequency.setValueAtTime(180, now + 0.1);
            gain.gain.setValueAtTime(0.15, now);
            gain.gain.linearRampToValueAtTime(0.001, now + 0.4);
            osc.start(now);
            osc.stop(now + 0.4);
        } else if (type === "whoosh") {
            // Transition whoosh using low frequency sine sweep
            osc.type = "sine";
            osc.frequency.setValueAtTime(100, now);
            osc.frequency.exponentialRampToValueAtTime(300, now + 0.3);
            gain.gain.setValueAtTime(0.08, now);
            gain.gain.linearRampToValueAtTime(0, now + 0.3);
            osc.start(now);
            osc.stop(now + 0.3);
        } else if (type === "ending") {
            // A triumphant retro sound
            osc.type = "sine";
            const notes = [523.25, 659.25, 783.99, 1046.50]; // C5, E5, G5, C6
            notes.forEach((freq, index) => {
                const noteOsc = audioCtx.createOscillator();
                const noteGain = audioCtx.createGain();
                noteOsc.type = "sine";
                noteOsc.frequency.setValueAtTime(freq, now + index * 0.1);
                noteGain.gain.setValueAtTime(0.08, now + index * 0.1);
                noteGain.gain.exponentialRampToValueAtTime(0.001, now + index * 0.1 + 0.3);
                noteOsc.connect(noteGain);
                noteGain.connect(audioCtx.destination);
                noteOsc.start(now + index * 0.1);
                noteOsc.stop(now + index * 0.1 + 0.35);
            });
        }
    } catch (e) {
        console.warn("Audio Context failed to play sound:", e);
    }
}

